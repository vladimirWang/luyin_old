DATASET_VERSION = "architecture-qa-v1"


def _summary(overview: str, key_points: list[str], actions: list[tuple[str, str, str]]) -> dict[str, Any]:
    return {
        "overview": overview,
        "keyPoints": [{"title": item} for item in key_points],
        "actionItems": [
            {"task": task, "owner": owner, "deadline": deadline}
            for task, owner, deadline in actions
        ],
    }


def _meeting(
    meeting_id: str,
    project_id: str,
    folder: str,
    name: str,
    day_offset: int,
    tag: str,
    speakers: dict[str, str],
    summary: dict[str, Any],
    transcript: list[tuple[str, str]],
) -> dict[str, Any]:
    segments = []
    for index, (speaker, text) in enumerate(transcript, 1):
        start_ms = (index - 1) * 48_000
        segments.append({
            "id": f"{meeting_id}-s{index:02d}",
            "startMs": start_ms,
            "endMs": start_ms + 42_000,
            "speaker": speaker,
            "text": text,
            "confidence": 0.96,
        })
    return {
        "id": meeting_id,
        "projectId": project_id,
        "folder": folder,
        "name": name,
        "dayOffset": day_offset,
        "tag": tag,
        "durationMs": len(segments) * 48_000,
        "speakers": speakers,
        "summary": summary,
        "segments": segments,
    }


